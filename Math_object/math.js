// JS provide math object for advanced mathematical calculation.

// Math.LN10;
// Math.ceil
// Math.floor
// Math.PI
// Math.round
// Math.random

let PI = Math.PI; // it prints the value of PI i.e.3.14
console.log(PI);

let log = Math.LN10;
console.log(log);

let five = -4.6;  // it rounds the value to the nearest integer value
console.log(Math.round(five));

let number = 4.1;  // it prints round up value of any integer
console.log(Math.ceil(number));

console.log(Math.floor(number));  // it prints the round down integer value

let value = Math.random();  // this function prints random value between 0 and 1
console.log(value);
console.log(Math.floor(value));
console.log(Math.ceil(value));